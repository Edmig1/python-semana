num = [5,3,9,2,1,0]
num.sort()
num2 = [8,4,9,2,6,0]
num2.sort(reverse=True)
print('lista 1 =',num)
print('lista 2 =',num2)
print('soma lista 1 =',sum(num),'Soma Lista 2 =',sum(num2))
print('maior lista 1 =',max(num),'maior lista 2 =',max(num2))
print('menor lista 1 =',min(num),'menor lista 2 =',min(num2))

